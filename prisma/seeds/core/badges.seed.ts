export async function seedBadges() {
  console.log('\nðŸ“› Seeding badges...');
  
  for (const badge of badges) {
    await prisma.badge.upsert({
      where: { type: badge.type },
      update: {
        name: badge.name,
        description: badge.description,
        criteria: badge.criteria,
        imageUrl: badge.imageUrl,
        category: badge.category,
        tier: badge.tier,
        points: badge.points,
        requiredCount: badge.requiredCount,
        criteriaConfig: badge.criteriaConfig,
      },
      create: badge,
    });
  }
  
  const count = await prisma.badge.count();
  console.log(`âœ… ${count} badges seeded successfully with dynamic criteria`);
}

async function main() {
  await seedBadges();
}

if (require.main === module) {
  main()
    .catch((e) => {
      console.error('âŒ Badge seed failed:', e);
      process.exit(1);
    })
    .finally(async () => {
      await prisma.$disconnect();
    });
}
