export const getProfileById = (id: string): UserProfile | undefined => {
  return userProfiles.find(profile => profile.id === id);
};

export const getProfilesByLocation = (location: string): UserProfile[] => {
  return userProfiles.filter(profile => 
    profile.location.toLowerCase().includes(location.toLowerCase()) ||
    profile.country.toLowerCase().includes(location.toLowerCase())
  );
};

export const getProfilesByInterest = (interest: string): UserProfile[] => {
  return userProfiles.filter(profile => 
    profile.interests.some(i => i.toLowerCase().includes(interest.toLowerCase()))
  );
};

export const getRandomProfiles = (count: number, excludeId?: string): UserProfile[] => {
  const filtered = excludeId ? userProfiles.filter(p => p.id !== excludeId) : userProfiles;
  return filtered.sort(() => 0.5 - Math.random()).slice(0, count);
};